function num()
{
	var n=prompt('Enter any number from 1-5');
	if(n==1)
	{
		alert('One');
	}
	else
		if(n==2)
		{
			alert('Two');
		}
		else
			if(n==3)
			{
				alert('Three');
			}
			else
				if(n==4)
				{
					alert('Four');
				}
				else
					if(n==5)
					{
						alert('Five');
					}
					else
						alert('Please enter a valid number');



}