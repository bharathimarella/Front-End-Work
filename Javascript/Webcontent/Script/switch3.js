function oscar()
{
	var y=prompt("Please enter the year");
	y=parseInt(y);
	switch(y)
	{
		case 2007:alert('No Country for Old Men');
		break;
		case 2008:alert('Slumdog Millionaire');
		break;
		case 2009:alert('The Hurt Locker');
		break;
		case 2010:alert('The Kings Speech ');
		break;
		case 2011:alert('The Artist');
		break;
		case 2012:alert('Argo ');
		break;
		case 2013:alert('12 Years a Slave');
		break;
		case 2014:alert('Birdman ');
		break;
		default:alert('Please enter the year between 2007-2014');
		
	}
}